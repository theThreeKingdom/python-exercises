# 牌型
# 1、豹子（炸弹）：三张点相同的牌。例：AAA、222
# 2、顺金（同花顺）：花色相同的顺子
# 3、金花（色皮）：花色相同，非顺子
# 4、顺子（拖拉机）：花色不同的顺子
# 5、对子：两张点数相同的牌。例：223，334
# 6、单张：三张牌不组成任何类型的牌。
# 7、特殊：花色不同的235
#
# 牌型比较
# 豹子 > 金花 > 顺子 > 对子 > 单张

